"""This class contains ANSI color styles that
can be used to format output of text on the terminal

"""
class text_format(object):
	CRED = '\033[91m'
	CEND = '\033[0m'
	BLINK = '\033[5m'
	CBOLD = '\033[1m'
	CITALIC   = '\033[3m'
	CURL      = '\033[4m'
	CBLINK    = '\033[5m'
	CBLINK2   = '\033[6m'
	CSELECTED = '\033[7m'
	CRED    = '\033[31m'
	CGREEN  = '\033[32m'
	CYELLOW = '\033[33m'
	CBLUE   = '\033[34m'

